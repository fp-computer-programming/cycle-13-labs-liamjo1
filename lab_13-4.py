# Create a list called day_date that contains the number day for the next 5 days, starting with "2" for today.
day_date = [2, 3, 4, 5, 6]

# Using a tuple, assign the 5 letters from the list to a variable that represents each day using a single statement.
thu, fri, mon, tue, wed = tuple(day_date)

# Print each of the variables (thu, fri, mon, tue, wed) to see what they equal.
print("Thursday:", thu)
print("Friday:", fri)
print("Monday:", mon)
print("Tuesday:", tue)
print("Wednesday:", wed)